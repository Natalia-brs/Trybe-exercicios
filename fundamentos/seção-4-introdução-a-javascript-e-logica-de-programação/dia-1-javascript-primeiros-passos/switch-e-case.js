//-------------- Exercicio switch-case --------------//
let state = 'Aprovada';
switch (state) {
    case 'Aprovada':
      console.log ('Parabens, voce foi aprovado (a)');
      break;
    case 'Reprovada':
      console.log ('Você foi reprovado');
    break;
    case 'lista':
      console.log ('Você está na lista');
    break;
    default:
      console.log ('não se aplica');     
}
